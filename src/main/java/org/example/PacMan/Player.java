    public void moveNormal(String direction){
        if (!(position.getY() == 117 && (position.getX() <= 22 || position.getX() >=160) && (direction.equals("up") || direction.equals("down")))){
            facingDirection = direction;
        }
        switch (facingDirection){
            case "up":
                position = moveUp();
                break;
            case "down":
                position = moveDown();
                break;
            case "left":
                position = moveLeft();
                break;
            case "right":
                position = moveRight();
                break;
        }
    }
